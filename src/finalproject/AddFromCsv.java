    public void appendToTextPanel(JTextPane jtp, String s) {
   try {
      Document doc = jtp.getDocument();
      doc.insertString(doc.getLength(), s, null);
   } catch(BadLocationException exc) {
      exc.printStackTrace();
   }
}

