    private void jButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartActionPerformed
        try {
            BufferedReader read = new BufferedReader(new FileReader(filename));
            String data;
            String strSubj;
            PreparedStatement prstmt;
            //connection = DriverManager.getConnection(ConnectionDB.url, ConnectionDB.login, ConnectionDB.pass);
            ConnectionDB db_conn = ConnectionDB.getConnectionDB();
            stmt = db_conn.connection.createStatement();
            int count = 0;
            while ((data = read.readLine()) != null) {
                appendToTextPanel(jTextPanelErrors, "\n" + data);
                String[] value = data.split(";");
                
                try {
                    if (value.length != 6)
                        throw new Exception("Неверное количество столбцов записи " +value.length + " - строка " + count);
                    StudentData student = new StudentData(value[0], value[1], value[2], value[3], value[4], value[5]);
                    if (student.msg.isEmpty()){
                        prstmt = connection.prepareStatement("Insert into " + ConnectionDB.db_name + ".Students Values(?, ?, ?, ?, ?, ?)");
                        prstmt.setInt(1, Integer.parseInt(student.id));
                        prstmt.setString(2, student.lastname);
                        prstmt.setString(3, student.name);
                        prstmt.setString(4, student.number);
                        prstmt.setString(5, student.city);
                        prstmt.setString(6, student.university);
                        prstmt.executeUpdate();
                        prstmt.close();
                    } else {
                        appendToTextPanel(jTextPanelErrors, "\n" + student.msg);
                    }    
                } catch (Exception e) {
                    //jTextPanelErrors.setText(jTextPanelErrors.getText() + e.getMessage() + "\n");
                    appendToTextPanel(jTextPanelErrors, "\n" + e.getMessage());
                }
                count++;
            }
            
        } catch (Exception e) {
            jTextPanelErrors.setText("Ошибка импорта файла: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonStartActionPerformed

